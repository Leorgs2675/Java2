    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = DriverManager.getConnection(strDbUrl);
            System.out.println("OK");
            
            String queryInsert = "INSERT INTO SinhVien VALUES(?,?,?,?,?,?)";
            PreparedStatement ps = conn.prepareStatement(queryInsert);
            ps.setString(1, txtMaSV.getText().trim());
            ps.setString(2, txtHoTen.getText().trim());
            ps.setString(3, txtEmail.getText().trim());
            ps.setString(4, txtSDT.getText().trim());
            ps.setInt(5, rdoNam.isSelected()?1:0);
            ps.setString(6, txtAreaDiaChi.getText().trim());
            
            ps.executeUpdate();
            
        }catch(Exception e){
            System.out.println("Loi ket noi "+ e);
        }
        loadDataToArray();
    }//GEN-LAST:event_btnSaveActionPerformed

