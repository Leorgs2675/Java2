    public void loadDataToArray(){
        try{
            Connection connect = DriverManager.getConnection(strDbUrl);
            System.out.println("OK");
            String query = "SELECT * FROM SinhVien";
            Statement stm = connect.createStatement();
            ResultSet resultSet = stm.executeQuery(query);
            while(resultSet.next()){
                Student std = new Student();
                std.setMaSV(resultSet.getString(1));
                std.setHoTen(resultSet.getString(2));
                std.setEmail(resultSet.getString(3));
                std.setSDT(resultSet.getString(4));
                std.setGioiTinh(resultSet.getBoolean(5));
                std.setDiaChi(resultSet.getString(6));
                list.add(std);
            }
        }
        catch(SQLException e){
            System.out.println("Error "+ e);
        }

        this.display(index);
    }
    
    public void display(int index){
        Student std = new Student();
        std = list.get(index);
        txtMaSV.setText(std.getMaSV());
        txtHoTen.setText(std.getHoTen());
        txtEmail.setText(std.getEmail());
        txtSDT.setText(std.getSDT());
        txtAreaDiaChi.setText(std.getDiaChi());
        
        if(std.isGioiTinh()){
            rdoNam.setSelected(true);
        }
        else rdoNu.setSelected(true);
    }
    
    

